#! /usr/bin/env perl

# === Perl keyword recognizer for the intuit_more regex heuristic ===
#
# Perl's S_intuit_more (toke.c) decides whether a `[` or `{` following a
# variable inside a pattern begins a subscript or a regex character-class /
# quantifier.  One branch of its weighting machine calls keyword(): if a run
# of consecutive alphas inside `[...]` spells a Perl keyword, that strongly
# implies it is NOT a character class (weight -= 150).
#
# Perl recognizes keywords with a generated trie (keywords.c, built by
# regen/keywords.pl via Devel::Tokenizer::C).  That file is GPL/Artistic and
# can't be copied into this MIT project.  The *names* of the keywords, however,
# are uncopyrightable facts, so we list them below and regenerate our own
# equivalent trie.  The list is the set of names keyword() can return, i.e.
# every KEY_* in perl's keywords.h (excluding the KEY_NULL "not a keyword"
# sentinel).
#
# To refresh against a newer perl:
#   grep -oE '^#define KEY_[A-Za-z0-9_]+' /path/to/perl5/keywords.h \
#     | sed 's/^#define KEY_//' | grep -vx NULL
# then paste the result into the __DATA__ section and re-run this script.

use v5.38;
use Path::Tiny qw/path/;

my @keywords =
  grep { length } map { chomp; s/^\s+|\s+$//gr } grep { /\S/ && !/^#/ } <DATA>;

# Group by length; within a length we emit a character-trie.
my %by_len;
push @{ $by_len{ length $_ } }, $_ for @keywords;

my @out;
