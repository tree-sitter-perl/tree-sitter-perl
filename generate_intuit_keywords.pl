sub char_literal {
  my ($c) = @_;
  # All keyword chars are [A-Za-z0-9_]; safe as plain C char literals.
  return "'$c'";
}

# Recursively emit a trie node for the set of @$words, all of length $len and
# sharing the first $depth characters.  $ind is the current indentation.
sub emit_node {
  my ($words, $depth, $len, $ind) = @_;

  if ($depth == $len) {
    push @out, "${ind}return true;\n";
    return;
  }

  # A single remaining word collapses to a flat char comparison instead of a
  # tower of one-case switches (keeps long unique keywords readable).
  if (@$words == 1) {
    my $w = $words->[0];
    my @cmp = map { "s[$_] == " . char_literal(substr($w, $_, 1)) } ($depth .. $len - 1);
    push @out, "${ind}return " . join(" && ", @cmp) . ";\n";
    return;
  }

  my %by_char;
  push @{ $by_char{ substr($_, $depth, 1) } }, $_ for @$words;

  push @out, "${ind}switch (s[$depth]) {\n";
  for my $ch (sort keys %by_char) {
    push @out, "${ind}  case ${\ char_literal($ch)}:\n";
    emit_node($by_char{$ch}, $depth + 1, $len, "${ind}    ");
  }
  push @out, "${ind}}\n";
  push @out, "${ind}return false;\n";
}

for my $len (sort { $a <=> $b } keys %by_len) {
  push @out, "    case $len:\n";
  emit_node($by_len{$len}, 0, $len, "      ");
